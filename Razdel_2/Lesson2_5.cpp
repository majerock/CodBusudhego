#include <iostream>
#include <cmath>

int main () {
    double m, final, n , k;
    std::cout << "Max donuts in pan: ";
    std::cin >> k ;
    std::cout << "How many donuts: ";
    std::cin >> n ;
    n=ceil(n);
    k=ceil(k);
    std::cout << "Frying time: ";
    std::cin >> m ;
    final = n/k;
    final = ceil(final);
    std::cout << "Final time: " << final*m*2;

}
